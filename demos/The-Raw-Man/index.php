<p>"Will there be anything else?" the waitress asked as she slipped the check onto the table.</p>
<p>"How long has that plant been up there?" the woman asked.</p>
<p>"A couple of weeks I think," the waitress said. "That one's about dead. Our manager buys new plants every month or so. He says it keeps the place looking fresh. Seems like a waste to me."</p>
<p>"Is there anyone I could talk to about taking it home with me?"</p>
<p>"No, he's not here today. You'd be better off just buying a new one, but as far as I'm concerned if you want it, you can have it," the waitress said as she looked down at the man's plate. "Is he finished?"</p>
<p>"Yeah, I think so," the woman said.</p>
<p>The woman walked over to the corner as the air conditioner clicked off and took the azalea down from a hook in the ceiling.</p>
<p>Plant in hand, she returned to the booth, gazed out the window at traffic on the road, and saw the man in his car, looking in the restaurant at her as he drove past.</p>
