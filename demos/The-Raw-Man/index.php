<p>"Don't turn around, but the man sitting behind us just picked his nose and wiped on his sleeve," the man said. "Probably one of those guys who holds a sign at road construction sites. He decides when to stop one side and let the other side go. He must have left his vest in the car though."</p>
<p>"I wonder if he gets to take the sign home with him," he continued. "I'll bet when he's at home and gets bored he goes out into his street and directs traffic."</p>
<p>The woman glanced over her shoulder and briefly looked across the booth at a bulky man shoveling his meal into his mouth. An audible click of the air conditioner's thermostat, that stopped the flow of cool air into the dining room, diverted her attention to the azalea, which slowly came to rest.</p>
<p>"Are we ready yet?" the woman asked.</p>
<p>"What's wrong, what, I'm not funny anymore," he said.</p>
<p>"It is about you after all," she said.</p>
