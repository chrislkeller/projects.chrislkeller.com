<p>"What are you talking about?" he asked.</p>
<p>"What are we going to do now?" she asked.</p>
<p>"What? Do now?"</p>
<p>"Nevermind."</p>
<p>As the waitress brought the couple their food and exchanged words with the man, the air conditioner clicked on and caused the azalea to spin again, but in the opposite direction. The woman looked at the plant and then looked deeply into the man's reflected eyes.</p>
<p>"You can't fix it Paul. You have to do things differently," she said.</p>
<p>"What more do you want from me? Don't I make you laugh and feel good when we're together?"</p>
 "Are things going to change or do we keep up this routine. Am I going to feel this way in a month from now? Two months from now?"</p>
<p>"This is who I am," he muttered. "I'm sorry if I'm not perfect but this is who I am."</p>
<p>"I need more out of us. I deserve more out of us, out of you."</p>
<p>"If you don't like it, you don't have to see me. We don't have to go out." he said.</p>
<p>The man reached into his lap and tossed the napkin onto his half-eaten sandwich.</p>
<p>"If you'll excuse me, I have to use the bathroom," he said.</p>
<p>The man left the booth and walked towards the lobby. The woman glanced at the plant and again at out the window.</p>
