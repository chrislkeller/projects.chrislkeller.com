<p>The woman watched it as he nervously became entertained with a napkin on the table.</p>
<p>"We have fun together? I thought we've had some fun. Remember that morning when we got up early and bought bread?" he asked. "We went down to the river and fed ducks and you ran towards that big group of them, you remember?"</p>
<p>"Yeah and they all flew off. 'Whack, whack, whack' they squawked off into the sky, making noise but not really doing anything, just whack, whack, whack" she said.</p>
<p>"YES - wasn't that fun? We squawked like ducks all day. Remember when we went to the grocery store and squawked in aisles to find each other. People must've thought we were nuts. I told you, we have fun together," he said.</p>
<p>"The plant hanging in the corner up there is dying," she said. "It should be budding by now; I wonder who would hang back here, inside a restaurant of all places."</p>
