<div class="first-entry" id="first-graf">
<p>Below an air conditioning vent, the azalea dangled in the corner of the half-empty dining room. Cold air blew forth from the vent, which caused the potted shrub to spin through the restaurant's lurid air permeated with stale cigarette smoke and greasy plates of fried food. A small amount of light filtered through windows, barely enough to ensure the leaves remained alive, and despite the wilted appearance, the buds continued their efforts to bloom.</p>
<p>Surrounded by patrons who ate patiently at tables a man and woman sat in a booth adjacent to a window. On the busy street outside, people traveling in cars combined with reflections of those seated inside. Above the din of the dinnertime banter, the woman listlessly studied the features of her companion's reflection on the window. Passing cars erased his face for a moment before it materialized a moment later. </p>
<p>Endlessly through the traffic, his features ebbed into view, faded and once again came into focus.</p>
