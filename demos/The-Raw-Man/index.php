<p>"Well is it about us? Is something wrong? What can I do to fix it?" he asked.</p>
<p>The waitress stopped by the booth, tightly across her chest hung an apron stained with remnants of her tasks and food brought to customers. The man stared widely as she asked if they wanted to order.</p>
<p>"Miss, could I have the turkey sandwich with Swiss cheese if I could, and could you make that on toast, but not too brown, <p>Yeah and some French fries before the sandwich, but could you make 'em crispy please. I don't like soggy fries. And to drink I'll just have water, but could I have a bigger glass?"</p>
<p>"Certainly," the waitress said as she turned to the woman. "And for you?"</p>
<p>"Could I just have an order of wheat toast...with butter please?"</p>
<p>"Is that all you want?" he asked. "To eat?"</p>
<p>"Just toast please Miss." the woman said.</p>
<p>The man's reflection loitered on the glass longer, left for passing cars to alter and twist in a mad dash of combinations before it realigned.</p>
